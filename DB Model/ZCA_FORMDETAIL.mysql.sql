CREATE TABLE ZCA_FORMDETAIL (
    CLNT CHAR(3) NOT NULL,
    FORM_GUID CHAR(32) NOT NULL,
    FORM_NAME VARCHAR(30),
    FORM_VER CHAR(4),
    INITIATED_BY VARCHAR(12),
    START_DATE DATE,
    START_TIME CHAR(6),
    LATEST_STARTDATE DATE,
    LATEST_STARTTIME CHAR(6),
    FORM_STATUS VARCHAR(22),
    PARENT_WF_ID CHAR(12),
    APPR_STEP VARCHAR(30),
    AGENT_ID_LIST VARCHAR(100),
    WORKCENTER_ID CHAR(8),
    FIRST_REM_DATE DATE,
    NEXT_REM_DATE DATE,
    NO_OF_REM_SENT SMALLINT,
    DEADLINE_DATE DATE,
    PRIMARY KEY (CLNT, FORM_GUID),
    FOREIGN KEY (FORM_NAME, FORM_VER) REFERENCES ZCA_FORM(FORM_NAME, FORM_VER)
);
